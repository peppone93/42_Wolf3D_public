#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	char	*str;
	int		i;

	str = (char *)s;
	i = 0;
	while (str[i] != '\0')
		i++;
	while (str[i] != (char)c)
	{
		if (i == 0)
			return (NULL);
		i--;
	}
	return (str + i);
}
