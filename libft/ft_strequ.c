#include "libft.h"

int		ft_strequ(char const *s1, char const *s2)
{
	int		i;

	if (s1 == NULL || s2 == NULL)
		return (0);
	i = ft_strcmp(s1, s2) == 0 ? 1 : 0;
	return (i);
}
