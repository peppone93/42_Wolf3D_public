#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	char *sc;

	sc = (char *)s;
	while (*sc != c && *sc != '\0')
		sc++;
	if (*sc == c)
		return (sc);
	else
		return (NULL);
}
