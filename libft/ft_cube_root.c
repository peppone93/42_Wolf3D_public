#include "libft.h"

double		ft_cube_root(double d)
{
	double ret;

	if (d > 0)
		ret = pow(d, 0.333333333333);
	else if (d < 0)
		ret = -pow(-d, 0.333333333333);
	else
		ret = 0;
	return (ret);
}
