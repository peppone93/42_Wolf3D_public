#include "vec.h"

t_vec3	v3_snr(t_vec3 v1)
{
	double	d;
	t_vec3	vn;

	vn = v3(0, 0, 0);
	if (v3_len(v1, vn) <= 0)
		return (vn);
	d = sqrt(v3_dot(v1, v1));
	return (v3_scd(v1, d));
}

t_vec3	mtov(double m[4][1])
{
	t_vec3 v;

	v = v3(m[0][0], m[1][0], m[2][0]);
	return (v);
}
